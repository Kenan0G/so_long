#include"libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{	
	t_list	*current_node;

	current_node = *lst;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	while (current_node->next != NULL)
		current_node = current_node->next;
	current_node->next = new;
}
